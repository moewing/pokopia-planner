const DICT: Record<Locale, MessageTree> = { zh, en };

// --- Hook -------------------------------------------------------------------

function lookup(tree: MessageTree, path: string): string | undefined {
  const parts = path.split(".");
  let cur: string | MessageTree = tree;
  for (const p of parts) {
    if (typeof cur === "string") return undefined;
    cur = cur[p];
    if (cur === undefined) return undefined;
  }
  return typeof cur === "string" ? cur : undefined;
}

function substitute(
  template: string,
  vars: Record<string, string | number> | undefined,
): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) => {
    const v = vars[k];
    return v === undefined ? `{${k}}` : String(v);
  });
}

export function useT() {
  const locale = useLocaleStore((s) => s.locale);

  const t = useCallback(
    (
      path: string,
      varsOrFallback?: Record<string, string | number> | string,
      maybeFallback?: string,
    ): string => {
      const vars =
        typeof varsOrFallback === "object" ? varsOrFallback : undefined;
      const fallback =
        typeof varsOrFallback === "string"
          ? varsOrFallback
          : maybeFallback;
      const raw =
        lookup(DICT[locale], path) ??
        lookup(DICT.zh, path) ??
        fallback ??
        path;
      return substitute(raw, vars);
    },
    [locale],
  );

  const translateEnv = useCallback(
    (e: Environment) => t(`env.${e}`, e),
    [t],
  );
  const translateTaste = useCallback(
    (t2: Taste) => t(`taste.${t2}`, t2),
    [t],
  );

  const translateSpecialty = useCallback(
    (s: string) => {
      if (locale === "zh") return s;
      return CONSTANTS.specialty_cn_to_en[s] ?? s;
    },
    [locale],
  );

  const translateLitteredItem = useCallback(
    (item: string) => t(`littered.${item}`, item),
    [t],
  );

  return {
    locale,
    t,
    translateEnv,
    translateTaste,
    translateSpecialty,
    translateLitteredItem,
  };
}
